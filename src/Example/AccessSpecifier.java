//Access Modifier Specifies where a property or a method is accessible.
//THere are 4 types of access modifiers
//Private
//Can only be accessed within the class, it cannot be accessed outside the class
//Default
//Can only be accessed within the package, it cannot be accessed within the package, if not specified then it will be default
//Protected
//Can only be accessed within the package and outside through the package through child class
//Public
//Public is everywhere, it can be accessed within the class, outside the class, within the package and outside the package